import math
ang = float(input('Informe um angulo'))
se = math.sin(ang)
co = math.cos(ang)
ta = math.tan(ang)
print('O valor do seno do angulo {}° é {:.2f}°'.format(ang, se))
print('O valor do coseno do angulo {}° é {:.2f}°'.format(ang, co))
print('O valor da tangente do angulo {}° é {:.2f}°'.format(ang, ta))

